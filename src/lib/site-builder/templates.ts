/**
 * Brand-aware text templates used to populate the generated ZIP archive
 * (Sprint 11.5).
 *
 * Pure functions — no Supabase / fs access here. All inputs come from
 * `SafeBrandContext`. Each template returns a UTF-8 string ready to be
 * written into a JSZip entry.
 */
import type { SafeBrandContext } from "./brandContext";

export function brandReadme(ctx: SafeBrandContext): string {
  const { profile, template } = ctx;
  return `# ${profile.brand_name} — Brand Site Kit

Generated by MARQ Site Builder (template: **${template.name}**, key: \`${template.key}\`).

This archive contains everything you need to spin up a standalone copy of the
"My Food Diary" storefront, rebranded for your business. Two paths:

## Option A — Remix the source project on Lovable

1. Open the source project: https://lovable.dev/projects/${template.source_project_id ?? "<source-project-id>"}
2. Click ⋯ → **Remix**.
3. In the new project, replace the brand assets with the contents of \`assets/\`.
4. Replace \`src/index.css\` color tokens with the values in \`theme.css\`.
5. Replace \`index.html\` head metadata with \`index.html\` from this archive.
6. Update environment variables: copy \`.env.example\` → \`.env\`, fill in your
   own Supabase URL + anon key (do **not** reuse the MARQ project credentials).
7. \`npm install && npm run build\` → deploy via Lovable Publish.

## Option B — Run locally

\`\`\`bash
unzip ${slug(profile.brand_name)}-site-kit.zip
cd ${slug(profile.brand_name)}-site-kit/integration
# Apply theme.css over your fork of the template
# Then build & deploy as usual
\`\`\`

## What is included

- \`BRAND.md\` — copy, contacts, palette, fonts (single source of truth for the rebrand)
- \`theme.css\` — drop-in OKLCH tokens for \`src/index.css\`
- \`index.html\` — pre-rebranded \`<head>\` with SEO + OG tags
- \`manifest.webmanifest\` — PWA manifest with brand colors & name
- \`package.json\` — minimal package metadata pre-filled with brand name
- \`.env.example\` — environment placeholders (no MARQ secrets)
- \`assets/README.md\` — instructions for placing logo / favicon / OG image

## What is NOT included (intentionally)

For security, the archive does **not** include any MARQ secrets, payment
gateway private keys, Resend API key, Supabase service-role key, Telegram
webhook secret, or files under \`.lovable/\`. You must provision your own
infrastructure for the rebranded site.

---
Generated at: ${new Date().toISOString()}
Source commit: ${template.source_commit ?? "n/a"}
`;
}

