export function assetsReadme(ctx: SafeBrandContext): string {
  const lines: string[] = [
    `# Assets for ${ctx.profile.brand_name}`,
    "",
    "Place the following image files in this folder before deploying:",
    "",
  ];
  if (ctx.profile.logo_url) lines.push(`- \`logo.png\` (downloaded from: ${ctx.profile.logo_url})`);
  else lines.push("- `logo.png` — your brand logo (recommended 512×512)");
  if (ctx.profile.favicon_url)
    lines.push(`- \`favicon.png\` (downloaded from: ${ctx.profile.favicon_url})`);
  else lines.push("- `favicon.png` — favicon (recommended 192×192)");
  if (ctx.profile.og_image_url)
    lines.push(`- \`og-default.jpg\` (downloaded from: ${ctx.profile.og_image_url})`);
  else lines.push("- `og-default.jpg` — social share image (recommended 1200×630)");
  return lines.join("\n") + "\n";
}

