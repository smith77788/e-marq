export function indexHtml(ctx: SafeBrandContext): string {
  const { profile } = ctx;
  const title = profile.tagline
    ? `${profile.brand_name} ‚Äî ${profile.tagline}`
    : profile.brand_name;
  const desc = profile.description || profile.tagline || profile.brand_name;
  const ogImage = profile.og_image_url || "";
  const lang = profile.locale === "en" ? "en" : "uk";
  return `<!doctype html>
<html lang="${escapeHtml(lang)}">
  <head>
    <meta charset="UTF-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>${escapeHtml(title)}</title>
    <meta name="description" content="${escapeHtml(desc)}" />
    <meta name="theme-color" content="${profile.primary_color.startsWith("#") ? profile.primary_color : "#1a1410"}" />

    ${profile.favicon_url ? `<link rel="icon" href="${escapeAttr(profile.favicon_url)}" />` : "<!-- favicon: add /favicon.ico -->"}
    ${profile.logo_url ? `<link rel="apple-touch-icon" href="${escapeAttr(profile.logo_url)}" />` : ""}
    <link rel="manifest" href="/manifest.webmanifest" />

    <meta property="og:title" content="${escapeHtml(title)}" />
    <meta property="og:description" content="${escapeHtml(desc)}" />
    <meta property="og:type" content="website" />
    <meta property="og:site_name" content="${escapeHtml(profile.brand_name)}" />
    <meta property="og:locale" content="${escapeAttr(lang === "en" ? "en_US" : "uk_UA")}" />
    ${ogImage ? `<meta property="og:image" content="${escapeAttr(ogImage)}" />` : ""}

    <meta name="twitter:card" content="summary_large_image" />
    <meta name="twitter:title" content="${escapeHtml(title)}" />
    <meta name="twitter:description" content="${escapeHtml(desc)}" />
    ${ogImage ? `<meta name="twitter:image" content="${escapeAttr(ogImage)}" />` : ""}
  </head>
  <body>
    <div id="root"></div>
    <script type="module" src="/src/main.tsx"></script>
  </body>
</html>
`;
}

export function manifestWebmanifest(ctx: SafeBrandContext): string {
  const { profile } = ctx;
  return JSON.stringify(
    {
      name: profile.brand_name,
      short_name: profile.brand_name.slice(0, 12),
      description: profile.description || profile.tagline || profile.brand_name,
      start_url: "/",
      display: "standalone",
      background_color: "#ffffff",
      theme_color: profile.primary_color.startsWith("#") ? profile.primary_color : "#1a1410",
      icons: [
        ...(profile.favicon_url
          ? [{ src: profile.favicon_url, sizes: "192x192", type: "image/png" }]
          : []),
        ...(profile.logo_url
          ? [{ src: profile.logo_url, sizes: "512x512", type: "image/png" }]
          : []),
      ],
    },
    null,
    2,
  );
}

